#!/usr/bin/env node

import { execFileSync } from "node:child_process";
import { mkdirSync, readFileSync, readdirSync, writeFileSync } from "node:fs";
import { tmpdir } from "node:os";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const desktopRoot = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const projectRoot = resolve(desktopRoot, "..");
const frontendRoot = join(desktopRoot, "frontend");
const outputPath = join(desktopRoot, "THIRD_PARTY_NOTICES.md");
const checkOnly = process.argv.includes("--check");

const noticeName = /^(licen[cs]e|copying|notice)(?:[._-].*)?$/i;

function noticeFiles(directory) {
  return readdirSync(directory, { withFileTypes: true })
    .filter((entry) => entry.isFile() && noticeName.test(entry.name))
    .map((entry) => entry.name)
    .sort((a, b) => a.localeCompare(b));
}

function indent(text) {
  return text
    .replace(/\r\n/g, "\n")
    .replace(/\s+$/, "")
    .split("\n")
    .map((line) => `    ${line}`)
    .join("\n");
}

function appendNotices(lines, directory, files) {
  for (const file of files) {
    lines.push(`#### ${file}`, "", indent(readFileSync(join(directory, file), "utf8")), "");
  }
}

function goModules() {
  const goCache = join(tmpdir(), "thaloca-go-build");
  mkdirSync(goCache, { recursive: true });
  const template = [
    "{{with .Module}}",
    "{{if ne .Path \"thaloca.local/thaloca\"}}",
    "{{printf \"%s\\t%s\\t%s\" .Path .Version .Dir}}",
    "{{end}}",
    "{{end}}",
  ].join("");

  const output = execFileSync(
    "go",
    ["list", "-deps", "-f", template, "./..."],
    {
      cwd: projectRoot,
      encoding: "utf8",
      env: { ...process.env, GOCACHE: goCache, GOFLAGS: "-mod=readonly" },
      stdio: ["ignore", "pipe", "inherit"],
    },
  );

  const modules = new Map();
  for (const line of output.split("\n")) {
    if (!line) continue;
    const [name, version, directory] = line.split("\t");
    if (!name || !directory) throw new Error(`Could not parse go list output: ${line}`);
    modules.set(`${name}@${version}`, { name, version, directory });
  }
  return [...modules.values()].sort((a, b) => a.name.localeCompare(b.name));
}

function npmPackages() {
  const lock = JSON.parse(readFileSync(join(frontendRoot, "package-lock.json"), "utf8"));
  const packages = [];

  for (const [packagePath, metadata] of Object.entries(lock.packages ?? {})) {
    if (!packagePath.startsWith("node_modules/") || metadata.dev === true) continue;
    packages.push({
      name: packagePath.slice("node_modules/".length),
      version: metadata.version ?? "unknown",
      license: metadata.license ?? "not declared in package-lock.json",
      directory: join(frontendRoot, packagePath),
    });
  }

  return packages.sort((a, b) => a.name.localeCompare(b.name));
}

function generate() {
  const lines = [
    "# Third-Party Notices",
    "",
    "This file contains notices for third-party Go modules compiled into the",
    "Thaloca executable and production npm packages included in the frontend",
    "dependency graph. It is generated by",
    "`scripts/generate-third-party-notices.mjs`; do not edit it manually.",
    "",
    "## Go modules compiled into Thaloca",
    "",
  ];

  for (const module of goModules()) {
    const files = noticeFiles(module.directory);
    if (files.length === 0) {
      throw new Error(`No license or notice file found for Go module ${module.name}@${module.version}`);
    }
    lines.push(`### ${module.name} ${module.version}`, "");
    appendNotices(lines, module.directory, files);
  }

  lines.push("## Production frontend packages", "");
  for (const pkg of npmPackages()) {
    const files = noticeFiles(pkg.directory);
    if (files.length === 0) {
      throw new Error(`No license or notice file found for npm package ${pkg.name}@${pkg.version}`);
    }
    lines.push(`### ${pkg.name} ${pkg.version}`, "", `Declared license: ${pkg.license}`, "");
    appendNotices(lines, pkg.directory, files);
  }

  return `${lines.join("\n").replace(/\n{3,}/g, "\n\n")}\n`;
}

const generated = generate();
if (checkOnly) {
  const current = readFileSync(outputPath, "utf8");
  if (current !== generated) {
    throw new Error("THIRD_PARTY_NOTICES.md is stale; regenerate it before packaging");
  }
} else {
  writeFileSync(outputPath, generated);
}
