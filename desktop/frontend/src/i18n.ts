// Minimal i18n helper — deliberately not a full framework: t(text) just
// looks up the literal English string in the current locale's dictionary
// and falls through to the English text unchanged if there's no entry yet,
// so a view can adopt t() one string at a time without a separate key
// namespace or a build-time completeness check. Persisted like theme.ts's
// preference (see that file) — a frontend-only choice, not a Go-backed
// setting.
export type Locale = 'en' | 'vi'

const STORAGE_KEY = 'thaloca-locale'

export const LOCALE_CHANGE_EVENT = 'thaloca:locale-change'

export function getLocale(): Locale {
  return localStorage.getItem(STORAGE_KEY) === 'vi' ? 'vi' : 'en'
}

export function setLocale(locale: Locale): void {
  localStorage.setItem(STORAGE_KEY, locale)
  document.dispatchEvent(new CustomEvent(LOCALE_CHANGE_EVENT))
}

